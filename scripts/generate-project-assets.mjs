import { mkdirSync } from "node:fs";
import { join } from "node:path";
import { spawnSync } from "node:child_process";

const outDir = join(process.cwd(), "public", "projects");
mkdirSync(outDir, { recursive: true });

const base = [
  "-size",
  "1400x1000",
  "xc:#F8F9FA",
  "-fill",
  "#EEF4FF",
  "-draw",
  "circle 1115,175 1305,175 circle 290,845 510,845",
  "-fill",
  "#FFFFFF",
  "-stroke",
  "#ECEFF3",
  "-strokewidth",
  "3",
  "-draw",
  "roundrectangle 145,115 1255,880 54,54"
];

function text(title, subject) {
  return [
    "-font",
    "DejaVu-Sans-Bold",
    "-fill",
    "#0B0B0B",
    "-pointsize",
    "28",
    "-draw",
    "text 160,90 'ProProjects.co'",
    "-pointsize",
    "54",
    "-draw",
    `text 160,932 '${title}'`,
    "-fill",
    "#3B82F6",
    "-pointsize",
    "24",
    "-draw",
    `text 160,970 '${subject}'`
  ];
}

function render(file, title, subject, commands) {
  const output = join(outDir, `${file}.png`);
  const result = spawnSync("convert", [...base, ...commands, ...text(title, subject), "-strip", "-quality", "92", output], {
    encoding: "utf8"
  });
  if (result.status !== 0) {
    throw new Error(`Failed to render ${file}: ${result.stderr}`);
  }
}

